f = open("numbers.txt", "r", encoding='UTF-8')
numstr = f.readline()
f.close()

max_seq = ""
for i in range(9):
    digits_re = re.compile(f"{i}+")
    for digit_seq in digits_re.findall(numstr):
        max_seq = digit_seq if len(digit_seq) > len(max_seq) else max_seq

print(f"Самая длинная последовательность одинковых цифр: {max_seq}")
